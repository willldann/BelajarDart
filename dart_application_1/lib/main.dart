// struktur dalam dart
void main() {

  print('hello world');


  /// type data int(bilangan bulat)
  int number = 10;

  print(number);


  /// type data double(bilangan desimal,)
  double number2 = 10.0;

  print(number2);


  /// type data string(text)
  String name = 'wildaan';

  print(name);


  /// type data bool(untuk menyatakan true or false)
  bool isValue = true;

  print(isValue);


  /// type data Map(key and value)
  Map person = {'name': 'wildan', 'age': '90'};

  print(person);
  print(person['name']);


  /// type data set(untuk yang unik,bisa banyak, just value)
  Set<String>  name3 = {'wildan','aqil','dll'};

  print(name3);

  /// type data set<int>
  Set<int> name4 = {10,20};

  print(name4);

  }
